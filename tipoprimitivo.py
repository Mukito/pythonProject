a = input('Digite um Algo: ')

print('O tipo primitivo de ', type(a)) #tipo digitado
print('So tem Espaços? ', a.isspace()) #espacos
print('É numerico? ', a.isnumeric()) #numerico
print('É Alfabetico? ', a.isalpha()) #Alfabettico
print('É maiusculas?', a.isupper() ) #maiuscula
print('É Minuscula?', a.islower()) #minuscula
print('É captalizada?', a.istitle()) #maiuscula e minusculas

